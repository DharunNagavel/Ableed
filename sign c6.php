<h2> Waking Up and Starting the Day</h2>
<li>The day begins with waking up in the morning.</li><br><li> In sign language, "morning" is signed by placing one arm flat across the body while the other hand rises up like the sun.</li><br><li> After waking up, people usually stretch, brush their teeth, and get ready for the day.</li><br><li> Each of these actions has its own sign, helping to describe a morning routine step by step.</li><br>

<h2>Eating and Work/School</h2>
<li>Meals are an essential part of the day.</li><br><li> The sign for "eat" is made by bringing your fingers together to your mouth.</li><br><li> Breakfast, lunch, and dinner are signed by combining "eat" with the respective time of day.</li><br><li> After eating, most people go to work or school.</li><br><li> The sign for "work" involves tapping one fist on top of the other, while "school" is signed by clapping both hands together twice.</li><br>

<h2>Afternoon Activities and Leisure</h2>
<li>In the afternoon, people continue working, studying, or taking breaks.</li><br><li> The sign for "afternoon" is made by positioning one arm at an angle over the other.</li><br><li> Some people use this time to exercise, read, or play games. The sign for "play" is done by forming "Y" shapes with both hands and shaking them slightly.</li><br>

<h2>Evening and Night Routine</h2>
<li>As the day comes to an end, people have dinner, spend time with loved ones, and relax.</li><br><li> The sign for "night" is made by placing a curved hand over the back of the other hand, symbolizing the sun setting.</li><br><li> Before bed, people may watch TV, read, or prepare for sleep.</li><br><li> The sign for "sleep" is made by pulling the hand down from the forehead while closing the fingers.</li><br>

