    <h1>Chapter 11: Work and Education Vocabulary</h1>
    <h2>Basic Signs for School, Work, and Learning</h2>
    <p>Here are simple nonverbal signs for talking about school, work, and related topics:</p>
    <h3>Education-Related Signs</h3>
    <p>School 🏫 – Clap both open hands together twice.<br>
       Teacher 👩‍🏫 – Place both hands near the forehead, then move them forward, followed by the "person" sign (flat hands moving downward).<br>
       Student 📖 – Use one hand to pick up an imaginary object from the palm, then drop it as if placing it in the mind.<br>
       Homework ✏️ – Sign "home" (touch fingertips to chin, then cheek) and "work" (tap fists together).</p>
       <h3>Work-Related Signs</h3>
       <p>ob 💼 – Fingerspell "J-O-B" or tap one fist on top of the other.<br>
Boss 👔 – Tap the fingertips of a clawed hand on the shoulder.<br>
Salary/Pay 💰 – Make a flat hand and swipe a closed fist across the palm.</p>
<br><p>Practicing these signs will help in nonverbal conversations about school, work, and daily life!</p>
<h3>Talking about what you do for work or study.</h3>
<p>👈 Point to yourself and sign "I work" (tap fists together).<br>
👉 Point to an imaginary workspace and sign your profession:<br>

Teacher 👩‍🏫 – Sign "teach" (both hands moving forward from the forehead) + "person" (flat hands moving downward).<br>
Doctor 🩺 – Tap "D" or "M" hand on the wrist, like checking a pulse.<br>
Engineer 🏗️ – Tap two "Y" hand shapes together.<br>
Boss 👔 – Tap a clawed hand on the shoulder.<br>

Job 💼 – Fingerspell "J-O-B" or tap fists toget</p>
<h3>Talking About Your Studies</h3>
<p>👈 Point to yourself and sign "I study" (one hand flat, the other fingers wiggling above it).
📖 Sign "student" (pick up an imaginary object from the palm, then drop it while doing the "person" sign).
🏫 Sign "school" (clap open hands together twice).
📚 Sign "homework" (sign "home" + "work").</p>
<h3> Asking About Someone Else’s Work or Studies</h3>
<p>🤔 Raise eyebrows and sign "Your work?" (point to the person + "work" sign).
👐 Gesture toward them while signing "What do you study?" (point + "study" + questioning face).<br><br>

By practicing these signs and expressions, you can easily communicate about work and education in nonverbal conversations!</p>
