<h1>Question Words in Sign Language</h2>
<li>In sign language, asking questions involves specific signs for words like "who," "what," "where," "when," "why," and "how." </li><br><li>The sign for "who" is made by placing the thumb on the chin and wiggling the index finger.</li><br><li> "What" is signed by holding both hands open with palms facing up and moving them slightly side to side.</li><br><li> "Where" is done by holding up the index finger and shaking it slightly.</li><br><li> These signs are essential for forming questions in sign language.</li><br>

<h1>Using Facial Expressions and Body Language</h1>
<li>Facial expressions and body language play a crucial role in sign language grammar.</li><br><li> When asking a question, it’s important to raise your eyebrows for "yes/no" questions and lower them for "wh-" questions.</li><br><li> For example, when signing "where," tilt your head slightly and furrow your brows to show that you’re asking a question.</li><br><li> Using body language helps clarify the meaning and adds natural flow to the conversation.</li><br>

<h1>Answering Questions in Sign Language</h1>
<li>When responding to questions, keep answers clear and direct. </li><br><li>If someone asks, "Where do you live?" you can respond with "I live [city name]" using proper sign structure.</li><br><li> Short answers like "yes" or "no" are often paired with head movements—nodding for "yes" and shaking the head for "no."</li><br><li> Adding extra details, like "I live in a house" or "I live with my family," helps improve communication skills.</li><br>

<h1>Practicing Q&A Conversations</h1>
<li>To improve fluency, practice asking and answering questions with a partner.</li><br><li> Take turns signing questions like "What is your favorite food?" or "How do you get to school?" Focus on using correct facial expressions and body language. </li><br><li>Engaging in real conversations helps build confidence and enhances understanding of sign language grammar.</li><br>
