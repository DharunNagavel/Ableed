    <h2>Hello, goodbye, thank you, please, sorry, yes, no.</h2>
    <h3>Essential Words and Phrases for Nonverbal Communication</h3>
    <p>For individuals who are nonverbal (dumb), using gestures, facial expressions, and simple signs can help communicate effectively. Here are some common words and their nonverbal equivalents:</p>
    <p><span class="sp">Hello 👋 – </span>A simple wave of the hand.</p>
    <p><span class="sp"> Goodbye 👋 – </span>A waving motion, moving the hand side to side.</p>
    <p><span class="sp">  Thank you 🙏 – </span>Place fingertips on the chin and move outward.</p>
    <p><span class="sp">  Please 🤲 – </span>Move a flat hand in a circular motion over the chest.</p>
    <p><span class="sp">Sorry 🤦‍♂️ – </span>Make a fist and rub it in a circular motion over the chest.</p>
    <p><span class="sp">  Yes 👍 –</span> A simple thumbs-up.</p>
    <p><span class="sp">  No 👎 – </span>A thumbs-down or a shaking of the head.</p>
    <br><br><p>Using gestures, facial expressions, or sign language can make communication smoother and more accessible in daily life!</p>
    <h3>Asking simple questions: "How are you?" "What is your name?"</h3>
    <h2>Asking Simple Questions Using Nonverbal Communication</h2>
    <p>For nonverbal communication, gestures, facial expressions, and simple signs can help ask common questions. Here are some easy ways to express them:</p>
    <p>"How are you?" 🤲👉😊 – Place your hand on your chest, then move it outward while raising your eyebrows for a questioning look.<br>
"What is your name?" ☝️🤔 – Point at the person, then gesture as if writing or use the sign for "name" (two fingers of each hand tapping together).<br>
"Where are you from?" 📍👉 – Point at the person, then use a gesture indicating a location or the sign for "from" (pulling one finger away from another).<br>
"Can you help me?" 🤝 – Make a helping gesture by placing one hand under the other as if lifting up.</p>
<br><br><p>Using facial expressions, hand movements, or sign language makes it easier to ask questions and engage in conversations!</p>
<h3>Role-Playing Common Greetings Using Nonverbal Communication</h3>
<p>Practicing greetings through role-playing helps improve confidence in nonverbal communication. Here are some simple scenarios to try with gestures, facial expressions, or sign language:</p>
<h3>Meeting Someone for the First Time</h3>
<p>👋 Wave hello and smile.</p>
<p>👉 Point to yourself and sign "My name is [name]" (tap two fingers together for “name”).</p>
<p>🤔 Raise eyebrows and sign "What is your name?"</p>

<h3> Greeting a Friend</h3>
<p>😊 Smile and wave enthusiastically.</p>
<p>👍 Give a thumbs-up for “How are you?” or sign it by placing your hand on your chest and moving it outward.</p>
<p>🤲 Sign "Good" or "Fine" with an open palm moving forward.</p>

<h3> Saying Goodbye</h3>
<p>👋 Wave your hand side to side for “Goodbye.”</p>
<p>🤝 Shake hands or give a thumbs-up to show appreciation.</p>
<p>🙏 Sign "Thank you" by moving fingertips from the chin outward.</p>

<p>Practicing these greetings with family, friends, or in front of a mirror helps build confidence in using nonverbal communication in everyday interactions!</p>


